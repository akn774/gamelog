function toHiragana(input: string): string {
  return input
    .normalize("NFKC") // å…¨è§’/åŠè§’ã‚’çµ±ä¸€
    .replace(/[\u30a1-\u30f6]/g, (match) =>
      String.fromCharCode(match.charCodeAt(0) - 0x60)
    );
}

